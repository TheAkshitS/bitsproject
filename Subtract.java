public class Subtract {

  public static void main(String[] args) {
    
    System.out.println("Subtracting 50 from 100");
    int first = 100;
    int second = 50;
    
    System.out.println(first + " " + second);

    // add two numbers
    int diff = first - second;
    System.out.println("The difference is: " + diff);
  }
}
